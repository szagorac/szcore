package com.xenaksys.szcore.font;

public class SmuflGlyphMeta {
    private String alternateCodepoint;
    private String codepoint;
    private String description;
    private String glyphName;

    public String getAlternateCodepoint() {
        return alternateCodepoint;
    }

    public void setAlternateCodepoint(String alternateCodepoint) {
        this.alternateCodepoint = alternateCodepoint;
    }

    public String getCodepoint() {
        return codepoint;
    }

    public void setCodepoint(String codepoint) {
        this.codepoint = codepoint;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getGlyphName() {
        return glyphName;
    }

    public void setGlyphName(String glyphName) {
        this.glyphName = glyphName;
    }
}
